// CMainFrame 메시지 처리기

void CMainFrame::OnSetFocus(CWnd* /*pOldWnd*/)
{
	// 뷰 창으로 포커스를 이동합니다.
	m_wndView.SetFocus();
}

BOOL CMainFrame::OnCmdMsg(UINT nID, int nCode, void* pExtra, AFX_CMDHANDLERINFO* pHandlerInfo)
{
	// 뷰에서 첫째 크랙이 해당 명령에 나타나도록 합니다.
	if (m_wndView.OnCmdMsg(nID, nCode, pExtra, pHandlerInfo))
		return TRUE;

	// 그렇지 않으면 기본 처리합니다.
	return CFrameWnd::OnCmdMsg(nID, nCode, pExtra, pHandlerInfo);
}


void CMainFrame::RecalcLayout(BOOL bNotify)
{
	// TODO: 여기에 특수화된 코드를 추가 및/또는 기본 클래스를 호출합니다.
	CRect	rect;
	GetClientRect(&rect);

	// CFrameWnd::RecalcLayout(bNotify);
	if (m_wndView.GetSafeHwnd() != NULL) {
		m_wndView.MoveWindow(0, 0, rect.Width() / 2, rect.Height() / 2);
	}
	if (m_wndView2.GetSafeHwnd() != NULL) {
		m_wndView2.MoveWindow(rect.Width() / 2, 0, rect.Width() / 2, rect.Height() / 2);
	}
	if (m_wndView3.GetSafeHwnd() != NULL) {
		m_wndView3.MoveWindow(0, rect.Height() / 2, rect.Width() / 2, rect.Height() / 2);
	}
	if (m_wndView4.GetSafeHwnd() != NULL) {
		m_wndView4.MoveWindow(rect.Width() / 2, rect.Height() / 2, rect.Width() / 2, rect.Height() / 2);
	}
}
